// Tag-vocabulary generator (C1).
//
// Extracts the CANONICAL Magic vocabulary from docs/comprehensive-rules.txt so
// our tag set is grounded in the actual rules, not memory:
//   - Keyword actions  (CR 701.x)  — verbs the engine performs (destroy, exile,
//                                     mill, scry, counter, fight, …)
//   - Keyword abilities (CR 702.x) — the ~190 named keywords (flying, trample,
//                                     deathtouch, cascade, …)
// Writes shared/src/tags.generated.ts. Re-run whenever the CR file is updated.
//
// Run:  node tools/gen-tag-vocab.mjs
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const CR = readFileSync(join(ROOT, "docs/comprehensive-rules.txt"), "utf8");

// A section like "702.3. Defender" (top-level entry only — the "702.3a" bodies
// are sub-rules, not names). Grab the first entry per number.
function extractNames(major) {
  const seen = new Map(); // minor -> name
  const re = new RegExp(`^${major}\\.(\\d+)\\. ([A-Z][A-Za-z' /-]+?)\\s*$`, "gm");
  let m;
  while ((m = re.exec(CR)) !== null) {
    const minor = Number(m[1]);
    const name = m[2].trim();
    // 701.1 / 702.1 are the prose intro paragraphs, not a keyword.
    if (minor === 1) continue;
    if (!seen.has(minor)) seen.set(minor, name);
  }
  return [...seen.entries()].sort((a, b) => a[0] - b[0]).map(([, n]) => n);
}

const keywordActions = extractNames("701");
const keywordAbilities = extractNames("702");

// slug: "Double Strike" -> "double_strike", "The Ring Tempts You" -> "the_ring_tempts_you"
const slug = (s) => s.toLowerCase().replace(/[^a-z0-9]+/g, "_").replace(/^_|_$/g, "");

const banner = `// AUTO-GENERATED by tools/gen-tag-vocab.mjs from docs/comprehensive-rules.txt.
// Do not edit by hand. Re-run the generator to refresh.
// Canonical Magic vocabulary — the grounding for our card-behaviour tags.`;

const out = `${banner}

// Keyword abilities — CR 702 (${keywordAbilities.length} entries).
export const KEYWORD_ABILITIES = ${JSON.stringify(keywordAbilities.map(slug), null, 0)} as const;
export type KeywordAbility = (typeof KEYWORD_ABILITIES)[number];

// Keyword actions — CR 701 (${keywordActions.length} entries).
export const KEYWORD_ACTIONS = ${JSON.stringify(keywordActions.map(slug), null, 0)} as const;
export type KeywordAction = (typeof KEYWORD_ACTIONS)[number];

// Human-readable display names, keyed by slug.
export const KEYWORD_ABILITY_NAMES: Record<string, string> = ${JSON.stringify(
  Object.fromEntries(keywordAbilities.map((n) => [slug(n), n])),
  null,
  2,
)};
export const KEYWORD_ACTION_NAMES: Record<string, string> = ${JSON.stringify(
  Object.fromEntries(keywordActions.map((n) => [slug(n), n])),
  null,
  2,
)};
`;

const outPath = join(ROOT, "shared/src/tags.generated.ts");
writeFileSync(outPath, out);
console.log(`Wrote ${outPath}`);
console.log(`  keyword abilities: ${keywordAbilities.length}`);
console.log(`  keyword actions:   ${keywordActions.length}`);
